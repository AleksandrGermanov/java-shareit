DROP TABLE IF EXISTS users, items, bookings, requests, comments;

CREATE TABLE IF NOT EXISTS users(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
	name VARCHAR(125),
	email varchar(125) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS items(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    owner_id BIGINT NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    name VARCHAR(125),
    description VARCHAR(125),
    available BOOL NOT NULL,
    request_id BIGINT
);

CREATE TABLE IF NOT EXISTS bookings(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    item_id BIGINT NOT NULL REFERENCES items(id) ON DELETE CASCADE,
    booker_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    start TIMESTAMP NOT NULL,
    ending TIMESTAMP NOT NULL,
    status VARCHAR(10),
    CONSTRAINT ending_gtr_start CHECK (ending > start)
);

CREATE TABLE IF NOT EXISTS requests(
id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
description VARCHAR(500) NOT NULL,
requester_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE
);

ALTER TABLE items ADD CONSTRAINT fk_request_id
FOREIGN KEY(request_id) REFERENCES requests(id) ON DELETE SET NULL;

CREATE TABLE IF NOT EXISTS comments(
id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
text VARCHAR(500) NOT NULL,
item_id BIGINT NOT NULL REFERENCES items(id) ON DELETE CASCADE,
author_id BIGINT NOT NULL REFERENCES users(id) ON DELETE NO ACTION,
created TIMESTAMP NOT NULL
);